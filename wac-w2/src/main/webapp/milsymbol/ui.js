function sidcCreatorInit(existingSidc) {

	value = existingSidc.charAt(1).toUpperCase();
	selection = document.getElementById('SIDCAFFILIATION');
	for (i in selection) {
		if (selection.options[i].value == value) {
			selection.selectedIndex = i;
			break;
		}
	}

	battledimensionValues();

	value = existingSidc.charAt(2).toUpperCase();
	selection = document.getElementById('SIDCBATTLEDIMENSION');
	for (i in selection) {
		if (selection.options[i].value == value) {
			selection.selectedIndex = i;
			break;
		}
	}

	value = existingSidc.charAt(3).toUpperCase();
	selection = document.getElementById('SIDCSTATUS');
	for (i in selection) {
		if (selection.options[i].value == value) {
			selection.selectedIndex = i;
			break;
		}
	}

	value = existingSidc.charAt(10).toUpperCase();
	selection = document.getElementById('SIDCSYMBOLMODIFIER11');
	for (i in selection) {
		if (selection.options[i].value == value) {
			selection.selectedIndex = i;
			break;
		}
	}

	functionIdValues();
	modifier12Values();

	value = existingSidc.charAt(11).toUpperCase();
	selection = document.getElementById('SIDCSYMBOLMODIFIER12');
	for ( var i in selection) {
		if (selection.options[i].value == value) {
			selection.selectedIndex = i;
			break;
		}
	}

	value = existingSidc.substring(4, 10).toUpperCase();
	selection = document.getElementById('SIDCFUNCTIONID');
	for ( var i in selection) {
		if (!isNaN(i)) {
			if (selection.options[i].value == value) {
				selection.selectedIndex = i;
				break;
			}
		}
	}

	// previewSymbol();
}
